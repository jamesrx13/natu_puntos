class EmpresaModel {
  final String id;
  final String address;
  final String name;
  final String city;
  final String departament;
  final String email;
  final String nit;
  final String phone;
  final String latitude;
  final String longitude;
  final String img;
  final String phoneToken;

  EmpresaModel({
